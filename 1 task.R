Task 1
Створити змінні базових (atomic) типів. Базові типи: character, numeric, integer, complex, logical.

character
ch <- 'a'
print(class(ch))
[1] "character"
numeric
n <- 78.9
print(class(n))
[1] "numeric"
integer
i <- 789L
print(class(i))
[1] "integer"
complex
c <- 78+9i
print(class(c))
[1] "complex"
logical
l <- TRUE
print(class(l))
[1] "logical"
